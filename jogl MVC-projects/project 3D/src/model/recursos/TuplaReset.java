package model.recursos;

public class TuplaReset {
	private double x;
	private double y;
	private double z;
	private double angulo;
	
	
	public TuplaReset(double x, double y, double z, double angulo){
		this.x = x;
		this.y = y;
		this.z = z;
		this.angulo = angulo;
	}
	
	public double getX() {
		return x;
	}


	public double getY() {
		return y;
	}


	public double getZ() {
		return z;
	}


	public double getAngulo() {
		return angulo;
	}
}
